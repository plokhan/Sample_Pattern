public class patternProgram {
	public class PatternPrograms {
	    public static void main(String[] args) {
	        System.out.println("Right-Angled Triangle Pattern:");
	        int rows = 5;
	        for (int i = 1; i <= rows; i++) {
	            for (int j = 1; j <= i; j++) {
	                System.out.print("*");
	            }
	            System.out.println();
	        }
	    }
	}
}